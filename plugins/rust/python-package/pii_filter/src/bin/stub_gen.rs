// Stub file generator for pii_filter module
//
// This binary generates Python type stub files (.pyi) for the pii_filter module.
// Run with: cargo run --bin stub_gen

use std::fs;
use std::path::Path;

use pii_filter_rust::stub_info;

const EXTENSION_STUB_PATH: &str = "cpex_pii_filter/pii_filter_rust/__init__.pyi";
const ORPHAN_EXTENSION_STUB_PATH: &str = "python/pii_filter_rust/__init__.pyi";
const GENERATED_ALL_MARKER: &str = "\"PIIDetectorRust\",\n]";
const CURATED_ALL_ENTRY: &str = "\"PIIDetectorRust\",\n    \"PIIFilterPluginCore\",\n]";
const PLUGIN_CORE_CLASS_MARKER: &str = "class PIIFilterPluginCore:";
const PLUGIN_CORE_CLASS_DEF: &str = "\n\n@typing.final\nclass PIIFilterPluginCore:\n    def __new__(cls, config: typing.Any) -> PIIFilterPluginCore: ...\n    def prompt_pre_fetch(self, payload: typing.Any, context: typing.Any) -> typing.Any: ...\n    def prompt_post_fetch(self, payload: typing.Any, context: typing.Any) -> typing.Any: ...\n    def tool_pre_invoke(self, payload: typing.Any, context: typing.Any) -> typing.Any: ...\n    def tool_post_invoke(self, payload: typing.Any, context: typing.Any) -> typing.Any: ...\n";

fn curate_extension_stub_content(content: &str) -> String {
    let mut curated = content.replace(GENERATED_ALL_MARKER, CURATED_ALL_ENTRY);
    if !curated.contains(PLUGIN_CORE_CLASS_MARKER) {
        curated.push_str(PLUGIN_CORE_CLASS_DEF);
    }

    assert!(
        curated.contains("\"PIIFilterPluginCore\""),
        "curated extension stub is missing PIIFilterPluginCore in __all__",
    );
    assert!(
        curated.contains(PLUGIN_CORE_CLASS_MARKER),
        "curated extension stub is missing PIIFilterPluginCore class definition",
    );

    curated
}

fn curate_extension_stub() {
    let stub_path = Path::new(EXTENSION_STUB_PATH);
    let content = fs::read_to_string(stub_path).expect("Failed to read generated stub file");
    let curated = curate_extension_stub_content(&content);
    fs::write(stub_path, curated).expect("Failed to write curated stub file");
}

fn remove_orphan_extension_stub() {
    let orphan_stub_path = Path::new(ORPHAN_EXTENSION_STUB_PATH);
    if orphan_stub_path.exists() {
        fs::remove_file(orphan_stub_path).expect("Failed to remove orphan extension stub file");
    }
}

fn curate_top_level_stub() {
    let stub_path = Path::new("cpex_pii_filter/__init__.pyi");
    let content = "# This file is automatically generated by pyo3_stub_gen\n# ruff: noqa: E501, F401, F403, F405\n\nfrom .pii_filter import PIIFilterPlugin\nfrom .pii_filter_rust import PIIDetectorRust\n\n__all__ = [\n    \"PIIDetectorRust\",\n    \"PIIFilterPlugin\",\n]\n";
    fs::write(stub_path, content).expect("Failed to write curated top-level stub file");
}

fn main() {
    // Get stub info (returns Result)
    let stub_info = stub_info().expect("Failed to get stub info");

    // Generate stub files - paths are determined from pyproject.toml
    stub_info.generate().expect("Failed to generate stub file");
    curate_top_level_stub();
    curate_extension_stub();
    remove_orphan_extension_stub();

    println!("✓ Generated stub files successfully");
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_curate_extension_stub_adds_required_exports_and_class() {
        let generated = "# This file is automatically generated by pyo3_stub_gen\n# ruff: noqa: E501, F401, F403, F405\n\nimport typing\n\n__all__ = [\n    \"PIIDetectorRust\",\n]\n";

        let curated = curate_extension_stub_content(generated);

        assert!(curated.contains("\"PIIFilterPluginCore\""));
        assert!(curated.contains(PLUGIN_CORE_CLASS_MARKER));
        assert!(curated.contains(CURATED_ALL_ENTRY));
    }

    #[test]
    #[should_panic(expected = "missing PIIFilterPluginCore in __all__")]
    fn test_curate_extension_stub_panics_when_expected_export_injection_fails() {
        let original = "# This file is automatically generated by pyo3_stub_gen\n# ruff: noqa: E501, F401, F403, F405\n\nimport typing\n\n__all__ = [\n    \"PIIDetectorRust\"\n]\n";

        let _ = curate_extension_stub_content(original);
    }
}
