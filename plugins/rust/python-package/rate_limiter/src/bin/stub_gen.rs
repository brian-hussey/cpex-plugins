// Generates Python type stubs (.pyi) for the rate_limiter_rust module.
// Run with: cargo run --bin stub_gen

use std::fs;
use std::path::Path;

use rate_limiter_rust::stub_info;

fn curate_extension_stub() {
    let stub_path = Path::new("cpex_rate_limiter/rate_limiter_rust/__init__.pyi");
    let mut content = fs::read_to_string(stub_path).expect("Failed to read generated stub file");
    content = content.replace(
        "\"RateLimiterEngine\",\n]",
        "\"RateLimiterEngine\",\n    \"RateLimiterPluginCore\",\n    \"compat_default_config\",\n    \"compat_parse_rate\",\n]",
    );
    if !content.contains("class RateLimiterPluginCore:") {
        content.push_str(
            "\n\n@typing.final\nclass RateLimiterPluginCore:\n    def __new__(cls, config: dict) -> RateLimiterPluginCore: ...\n    def prompt_pre_fetch(self, payload: typing.Any, context: typing.Any) -> typing.Any: ...\n    def tool_pre_invoke(self, payload: typing.Any, context: typing.Any) -> typing.Any: ...\n",
        );
    }
    if !content.contains("def compat_default_config()") {
        content.push_str(
            "\n\ndef compat_default_config() -> dict: ...\ndef compat_parse_rate(rate: builtins.str) -> tuple[builtins.int, builtins.int]: ...\n",
        );
    }
    fs::write(stub_path, content).expect("Failed to write curated stub file");
}

fn curate_top_level_stub() {
    let stub_path = Path::new("cpex_rate_limiter/__init__.pyi");
    let content = "# This file is automatically generated by pyo3_stub_gen\n# ruff: noqa: E501, F401, F403, F405\n\nfrom .rate_limiter import RateLimiterConfig, RateLimiterPlugin, _parse_rate\n\n__all__ = [\n    \"RateLimiterConfig\",\n    \"RateLimiterPlugin\",\n    \"_parse_rate\",\n]\n";
    fs::write(stub_path, content).expect("Failed to write curated top-level stub file");
}

fn main() {
    let stub_info = stub_info().expect("Failed to get stub info");
    stub_info.generate().expect("Failed to generate stub file");
    curate_top_level_stub();
    curate_extension_stub();
    println!("✓ Generated stub files successfully");
}
