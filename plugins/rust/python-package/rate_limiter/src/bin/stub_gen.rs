// Generates Python type stubs (.pyi) for the rate_limiter_rust module.
// Run with: cargo run --bin stub_gen

use std::fs;
use std::path::Path;

use rate_limiter_rust::stub_info;

fn strip_blank_line_trailing_whitespace(content: &str) -> String {
    content
        .lines()
        .map(|line| if line.trim().is_empty() { "" } else { line })
        .collect::<Vec<_>>()
        .join("\n")
        + "\n"
}

fn curate_extension_stub() {
    let stub_path = Path::new("cpex_rate_limiter/rate_limiter_rust/__init__.pyi");
    let mut content = fs::read_to_string(stub_path).expect("Failed to read generated stub file");
    content = content.replace(
        "\"RateLimiterEngine\",\n]",
        "\"RateLimiterEngine\",\n    \"RateLimiterPluginCore\",\n    \"compat_default_config\",\n    \"compat_parse_rate\",\n]",
    );
    if !content.contains("class RateLimiterPluginCore:") {
        content.push_str(
            "\n\n@typing.final\nclass RateLimiterPluginCore:\n    def __new__(cls, config: dict) -> RateLimiterPluginCore: ...\n    def prompt_pre_fetch(self, payload: typing.Any, context: typing.Any) -> typing.Any: ...\n    def tool_pre_invoke(self, payload: typing.Any, context: typing.Any) -> typing.Any: ...\n",
        );
    }
    if !content.contains("def compat_default_config()") {
        content.push_str(
            "\n\ndef compat_default_config() -> dict: ...\ndef compat_parse_rate(rate: builtins.str) -> tuple[builtins.int, builtins.int]: ...\n",
        );
    }
    content = strip_blank_line_trailing_whitespace(&content);
    fs::write(stub_path, content).expect("Failed to write curated stub file");
}

fn curate_top_level_stub() {
    let stub_path = Path::new("cpex_rate_limiter/__init__.pyi");
    let content = "# This file is automatically generated by pyo3_stub_gen\n# ruff: noqa: E501, F401, F403, F405\n\nfrom .rate_limiter import RateLimiterConfig, RateLimiterPlugin, _parse_rate\n\n__all__ = [\n    \"RateLimiterConfig\",\n    \"RateLimiterPlugin\",\n    \"_parse_rate\",\n]\n";
    fs::write(stub_path, content).expect("Failed to write curated top-level stub file");
}

fn main() {
    let stub_info = stub_info().expect("Failed to get stub info");
    stub_info.generate().expect("Failed to generate stub file");
    curate_top_level_stub();
    curate_extension_stub();
    println!("✓ Generated stub files successfully");
}

#[cfg(test)]
mod tests {
    use super::{curate_extension_stub, strip_blank_line_trailing_whitespace};
    use std::fs;
    use std::path::PathBuf;
    use std::sync::{Mutex, OnceLock};

    fn cwd_lock() -> &'static Mutex<()> {
        static LOCK: OnceLock<Mutex<()>> = OnceLock::new();
        LOCK.get_or_init(|| Mutex::new(()))
    }

    fn temp_project_dir(test_name: &str) -> PathBuf {
        let mut path = std::env::temp_dir();
        path.push(format!(
            "cpex-rate-limiter-stub-test-{}-{}",
            test_name,
            std::process::id()
        ));
        let _ = fs::remove_dir_all(&path);
        fs::create_dir_all(path.join("cpex_rate_limiter/rate_limiter_rust")).unwrap();
        path
    }

    #[test]
    fn strip_blank_line_trailing_whitespace_preserves_nonblank_lines() {
        let content =
            "class Example:\n    r\"\"\"\n    keep indentation\n    \n        \n    done\n";

        assert_eq!(
            strip_blank_line_trailing_whitespace(content),
            "class Example:\n    r\"\"\"\n    keep indentation\n\n\n    done\n"
        );
    }

    #[test]
    fn curate_extension_stub_adds_compat_exports_and_plugin_core() {
        let _guard = cwd_lock().lock().unwrap();
        let original_cwd = std::env::current_dir().unwrap();
        let temp_dir = temp_project_dir("extension");
        let stub_path = temp_dir.join("cpex_rate_limiter/rate_limiter_rust/__init__.pyi");
        fs::write(
            &stub_path,
            "# This file is automatically generated by pyo3_stub_gen\n\n__all__ = [\n    \"RateLimiterEngine\",\n]\n\nclass RateLimiterEngine:\n    pass\n",
        )
        .unwrap();

        std::env::set_current_dir(&temp_dir).unwrap();
        curate_extension_stub();
        std::env::set_current_dir(original_cwd).unwrap();

        let content = fs::read_to_string(&stub_path).unwrap();
        assert!(content.contains("\"RateLimiterPluginCore\""));
        assert!(content.contains("\"compat_default_config\""));
        assert!(content.contains("\"compat_parse_rate\""));
        assert!(content.contains("class RateLimiterPluginCore:"));
        assert!(content.contains("def compat_default_config() -> dict: ..."));
        assert!(content.contains(
            "def compat_parse_rate(rate: builtins.str) -> tuple[builtins.int, builtins.int]: ..."
        ));

        fs::remove_dir_all(temp_dir).unwrap();
    }
}
